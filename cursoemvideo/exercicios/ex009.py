# faça um programa que leia um numero
# inteiro qualquer e mostre na tela sua tabuada
num = int(input('Digite um numero inteiro: '))
zero = num * 0
um = num * 1
dois = num * 2
tres = num * 3
quatro = num * 4
cinco = num * 5
seis = num * 6
sete = num * 7
oito = num * 8
nove = num * 9
dez = num * 10
print('A tabuada de {} é:'. format(num))
print('{} x 0 = {}'.format(num, zero))
print('{} x 1 = {}'.format(num, um))
print('{} x 2 = {}'.format(num, dois))
print('{} x 3 = {}'.format(num, tres))
print('{} x 4 = {}'.format(num, quatro))
print('{} x 5 = {}'.format(num, cinco)) 
print('{} x 6 = {}'.format(num, seis)) 
print('{} x 7 = {}'.format(num, sete))
print('{} x 8 = {}'.format(num, oito)) 
print('{} x 9 = {}'.format(num, nove))  
print('{} x 10 = {}'.format(num, dez))
